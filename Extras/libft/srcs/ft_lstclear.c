#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;

	while (*lst)
	{
		temp = *lst;
		(*lst) = (*lst)->next;
		(*del)(temp->content);
		free(temp);
	}
	*lst = (NULL);
}
