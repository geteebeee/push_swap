char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	v;

	v = (char)c;
	i = 0;
	while (s[i] != '\0' && s[i] != v)
	{
		i++;
	}
	if (s[i] == v)
		return ((char *)&s[i]);
	return (0);
}
