char	*ft_strrchr(const char *s, int c)
{
	int		i;
	int		j;
	char	v;

	v = (char)c;
	i = 0;
	j = -1;
	while (s[i] != '\0')
	{
		if (s[i] == v)
			j = i;
		i++;
	}
	if (v == '\0')
	{
		return ((char *)&s[i]);
	}
	if (j != -1)
		return ((char *)&s[j]);
	return (0);
}
