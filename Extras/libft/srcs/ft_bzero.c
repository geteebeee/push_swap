#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ps;

	ps = (char *)s;
	i = 0;
	while (n > 0)
	{
		ps[i] = 0;
		i++;
		n--;
	}
}
